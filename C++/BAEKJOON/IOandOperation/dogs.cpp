#include <iostream>
using namespace std;

// 백슬래시, 따옴표 "" 등을 출력하기 위해서는
// 반드시 앞에 \ 을 포함해야만 한다.
int main(){

    cout << "|\\_/|" << endl;
    cout << "|q p|   /}" << endl;
    cout << "( 0 )\"\"\"" << "\\" << endl;
    cout << "|\"^\"`    |" << endl;
    cout << "||_/=\\\\__|" << endl;    

    return 0;
}